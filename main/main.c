void app_main() {
    //Initialize NVS
    esp_err_t ret = nvs_flash_init();
    if (ret == ESP_ERR_NVS_NO_FREE_PAGES || ret == ESP_ERR_NVS_NEW_VERSION_FOUND) {
      ESP_ERROR_CHECK(nvs_flash_erase());
      ret = nvs_flash_init();
    }
    ESP_ERROR_CHECK(ret);

    ESP_LOGI(TAG, "ESP_WIFI_MODE_STA");
    wifi_init_sta();

    udp_logging_init( CONFIG_LOG_UDP_IP, CONFIG_LOG_UDP_PORT, udp_logging_vprintf );
    udp_receiver_init( CONFIG_LOG_UDP_IP, CONFIG_FLASH_UDP_PORT);
    while (1) {
        vTaskDelay(1000 / portTICK_PERIOD_MS);
        ESP_LOGI(TAG, "Test logI");
        ESP_LOGD(TAG, "Test logD");
        ESP_LOGE(TAG, "Test logE");
        ESP_LOGI(TAG, "Test logW");
        ESP_LOGE(TAG, "Test logV");
        printf("Printing received data\n");
        udp_receiver_receive();
    }
    // xTaskCreate(&ota_server_task, "ota_server_task", 4096, NULL, 5, NULL);
}
